        <h2>Pasemos a repasar de forma abreviada cómo funciona cada una de las cinco fuerzas.</h2>

        <h2>Amenaza de nuevos entrantes.</h2>

        <p>
        La aparición de nuevas empresas en el sector supone un incremento de recursos, de capacidad y, en principio, un intento de obtener una participación en el mercado a costa de otros que ya la tenían. La posibilidad de entrar en un sector depende fundamentalmente de dos factores: la capacidad de reacción de las empresas que ya están (tecnológica, financiera, productiva, etc.) y las denominadas barreras de entrada (obstáculos para el ingreso). Entre las barreras de entrada, las más características son:
        </p>

        <ul>
            <li><strong>Economía de escala:</strong> Reducción de costes unitarios debido al volumen (vinculada a menudo a reducciones por efecto experiencia), como por ejemplo, coches, aviones...</li>
            <li><strong>Grado de diferenciación del producto/servicio:</strong> La fidelidad de los clientes obliga a realizar inversiones muy grandes (y arriesgadas) para desalojar al suministrador tradicional. Crítico en los mercados en los que la confianza es fundamental (bancos, farmacéuticas, etc.).</li>
            <li><strong>Necesidades de capital:</strong> Las necesidades de capital, especialmente cuando éste tiene que ser desembolsado inicialmente o su recuperación, en caso de fallo, es difícil, constituye una barrera muy importante (coches, acero, etc.).</li>
            <li><strong>Costes de cambio:</strong> Existen multitud de productos y servicios en los que el comprador tiene que asumir un coste extra si quiere cambiar de proveedor, principalmente por aspectos logísticos (entrenamiento, repuestos, almacenes, etc.).</li>
            <li><strong>Acceso a los canales de distribución:</strong> El control de los canales de distribución puede dificultar seriamente el acceso a un mercado. El canal puede cargar sobreprecios y los competidores bajar los suyos.</li>
        </ul>

        <p>
            <strong>Otros factores:</strong> Dentro de este apartado podemos incluir las patentes, el acceso privilegiado a materias primas, la ubicación, las ayudas gubernamentales, etc.
        </p>


        <h2>Rivalidad de los competidores</h2>

        <p>
            La rivalidad aparece cuando uno o varios competidores sienten la presión o ven la oportunidad de mejorar. El grado de rivalidad depende de una serie de factores estructurales, entre los que podemos destacar:
        </p>

        <ul>
            <li><strong>Gran número de competidores, o competidores muy equilibrados.</strong></li>
            <li><strong>Crecimiento lento en el mercado:</strong> Cuando los mercados se estancan, la única forma de mejorar los resultados propios es arrebatar cuota a la competencia.</li>
            <li><strong>Costes fijos o de almacenamiento elevados:</strong> Al darse esa situación, es necesario hacer un gran esfuerzo para operar a plena capacidad, o al menos por encima del punto muerto.</li>
            <li><strong>Baja diferenciación de productos:</strong> El consumidor se ve atraído por el precio, y los competidores tenderán a bajarlo.</li>
            <li><strong>Intereses estratégicos:</strong> En determinados mercados, puede ocurrir que varias empresas importantes intenten, de forma simultánea, establecer una posición sólida y utilicen para ello recursos desproporcionados.</li>
            <li><strong>Barreras de salida:</strong> Cuando los competidores tienen dificultades para salir de un mercado que ha perdido interés, mantendrán una intensidad competitiva alta, si las barreras de salida son importantes. Entre las barreras de salida podemos destacar los activos especializados, los costes fijos de salida, las restricciones sociales o las barreras emocionales.</li>
        </ul>

        <h2>Presión de los productos sustitutivos</h2>

        <p>
            El nivel de precio/calidad de los productos sustitutivos limita el nivel de precios de la industria. Los productos sustitutivos pueden ser fabricados por empresas pertenecientes o ajenas al sector (situación peligrosa). Las empresas del sector pueden reaccionar en bloque, no hacerlo en absoluto, o cambiar de necesidad satisfecha adaptando el producto (un crucero no puede competir con el avión en el transporte de viajeros, pero es un medio de vacaciones de lujo inigualable). Desde la óptica estratégica, hay que prestar mucha atención a los “sustitutivos no evidentes” (ejemplo, videoconferencia contra hotel más avión).
        </p>

        <h2>Poder de negociación de los compradores/clientes</h2>

        <p>
            Los compradores fuerzan los precios a la baja y la calidad al alza, en perjuicio del beneficio de la industria. Su poder aumenta si:
        </p>

        <ul>
            <li><strong>Están concentrados, o compran grandes volúmenes relativos.</strong></li>
            <li><strong>El coste de la materia prima es importante.</strong></li>
            <li><strong>Los productos no son diferenciados.</strong></li>
            <li><strong>El coste de cambiar de proveedor es pequeño.</strong></li>
            <li><strong>No hay amenaza de integración.</strong></li>
            <li><strong>Tienen información total.</strong></li>
            <li><strong>La calidad no es importante.</strong></li>
        </ul>

        <h2>Poder de negociación de los proveedores</h2>

        <p>
            Los proveedores poderosos pueden amenazar con subir los precios y/o disminuir la calidad. Las empresas del sector pueden ver disminuidos sus beneficios si no consiguen repercutir los incrementos al consumidor final. Su poder aumenta si:
        </p>

        <ul>
            <li><strong>Está más concentrado que el sector que compra.</strong></li>
            <li><strong>No están obligados a competir con sustitutivos.</strong></li>
            <li><strong>El comprador no es cliente importante.</strong></li>
            <li><strong>El producto es importante para el comprador.</strong></li>
            <li><strong>El producto está diferenciado.</strong></li>
            <li><strong>Representan una amenaza de integración.</strong></li>
        </ul>

