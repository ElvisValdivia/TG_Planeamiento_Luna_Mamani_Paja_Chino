            <div class="arrow">→</div>
            <div class="flex-item">Valor obtenido por la empresa en relación a los costes</div>
        </div>

        <h2>Las Actividades Primarias son aquellas que tienen que ver con el producto/servicio, su producción,
            logística, comercialización, etc. </h2>
        <ul>
            <li>Logística de entrada (recepción, almacenamiento, manipulación de materiales, inspección, etc.)</li>
            <li>Operaciones (procesos de fabricación, ensamblaje, mantenimiento, etc.)</li>
            <li>Logística de salida (gestión de pedidos, almacenamiento, transporte, etc.)</li>
            <li>Marketing y ventas (comercialización, selección de canales de distribución, etc.)</li>
            <li>Servicios (instalación, mantenimiento, reparaciones, atención postventa, etc.)</li>
        </ul>

        <h2>Las Actividades de Soporte o apoyo a las actividades primarias son: </h2>
        <ul>
            <li>Infraestructura empresarial (administración, finanzas, control de calidad, etc.)</li>
            <li>Gestión de recursos humanos (selección, contratación, formación, incentivos)</li>
            <li>Desarrollo tecnológico (automatización, desarrollo de procesos, investigación, etc.)</li>
            <li>Abastecimiento (compras de materias primas, suministros, etc.)</li>
        </ul>

        <p>
            <strong>El Margen</strong> es la diferencia entre el valor total obtenido y los costes incurridos por la
            empresa
            para desempeñar las actividades generadoras de valor.
        </p>

       

