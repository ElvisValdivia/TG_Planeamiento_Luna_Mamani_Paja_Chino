        <h1>6. Análisis Interno: La Cadena de Valor</h1>
        <p>Todas las actividades de una empresa forman la cadena de valor.</p>
        <p>
            La Cadena de Valor es una herramienta que permite a la empresa identificar aquellas actividades o fases
            que pueden aportar un mayor valor añadido al producto final. Intentar buscar fuentes de ventaja competitiva.
        </p>
        <p>
            La empresa está formada por una secuencia de actividades diseñadas para añadir valor al producto o servicio
            según
            las distintas fases, hasta que se llega al cliente final.
        </p>
        <p>Una cadena de valor genérica está constituida por tres elementos básicos:</p>

        <!-- Representación de las Actividades Primarias y de Apoyo -->
