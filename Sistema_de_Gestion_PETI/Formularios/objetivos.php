        <p>En empresas de gran tamaño, se pueden formular los objetivos estratégicos en fuw ew nción de sus diferentes unidades estratégicas de negocio (UEN). Estas UEN  se hacen especialmente necesarias en las empresas diversificadas o con  multiactividad donde la heterogeneidad de los distintos negocios hace inviable un tratamiento estratégico conjunto de los mismos.

            Se entiende por unidad estratégica de negocio (UEN) ("strategic business unit" [SBU]) un conjunto homogéneo de actividades o negocios, desde el punto de vista estratégico, es decir, para el cual es posible formular una estrategia común y a su vez diferente de la estrategia adecuada para otras actividades y/o unidades estratégicas. 
            La estrategia de cada unidad es autónoma, pero no independiente de las demás unidades estratégicas, puesto que se integran en la estrategia de la empresa.</p>   

            <h4 style="text-align: center;">¿Cómo podemos identificar a las UEN?</h4>
          
            <p> La identificación de las UEN se puede realizar a partir de las tres siguientes dimensiones:</p> 
                <p>•<strong> Grupos de clientes:</strong> Que atiende al tipo de clientela al cual va destinado el producto o servicio.</p>
                <p>•<strong> Funciones:</strong> Necesidades cubiertas por el producto o servicio.</p>
                <p>•<strong> Tecnología:</strong> Forma en la cual la empresa cubre a través del producto o servicio la necesidad de la clientela.</p>

                
