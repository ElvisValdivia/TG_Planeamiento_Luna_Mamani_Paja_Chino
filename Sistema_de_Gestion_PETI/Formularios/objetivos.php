            <p>El siguiente paso es establecer los objetivos de una empresa en relación al sector al que pertenece.</p>
            <p>Un <strong>OBJETIVO ESTRATÉGICO</strong> es un fin deseado, clave para la organización y para la consecución de su visión. Para una correcta planificación construya los objetivos formando una pirámide. Los objetivos de cada nivel indican qué es lo que quiere lograrse, siendo la estructura de objetivos que está en el nivel 
                inmediatamente inferior la que indica el cómo. Por tanto, cada objetivo es un fin en sí mismo, pero también a la vez un medio para el logro de los objetivos del nivel superior.</p>

            <p><strong>Objetivos estratégicos:</strong> Concretan el contenido de la misión. Suelen referirse al crecimiento, rentabilidad y a la sostenibilidad de la empresa. Su horizonte es entre 3 a 5 años.</p>
            <p><strong>Objetivos operativos:</strong> Son la concreción anual de los objetivos estratégicos. Han de ser claros, concisos y medibles. Se puede distinguir dos tipos de objetivos específicos:</p>
            <ol>
                <li><strong>Funcionales:</strong> objetivos formulados por áreas o departamentos.</li>
                <li><strong>Operativos:</strong> objetivos que se centran en operaciones y acciones concretas.</li>
            </ol>

            <p>Cualquier objetivo formulado tiene que presentar los siguientes atributos:</p>

