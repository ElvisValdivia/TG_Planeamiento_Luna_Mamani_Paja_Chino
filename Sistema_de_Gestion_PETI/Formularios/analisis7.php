        <h1>7. ANÁLISIS INTERNO: MATRIZ DE CRECIMIENTO - PARTICIPACIÓN BCG</h1>
    </div>

    <p>Toda empresa debe analizar de forma periódica su cartera de productos y servicios. La <strong>Matriz de crecimiento - participación</strong>, conocida como Matriz <strong>BCG</strong>, es un método gráfico de análisis de cartera de negocios desarrollado por The Boston Consulting Group en la década de 1970. Su finalidad es ayudar a priorizar recursos entre distintas áreas de negocios o Unidades Estratégicas de Análisis (UEA), es decir, para determinar en qué negocios debo invertir, desinvertir o incluso abandonar. Se trata de una sencilla matriz con cuatro cuadrantes, cada uno de los cuales propone una estrategia diferente para una unidad de negocio. Cada cuadrante viene representado por una figura o ícono.</p>

    <p>El eje vertical de la matriz define el crecimiento en el mercado, y el horizontal la cuota de participación relativa en el mercado.</p>

